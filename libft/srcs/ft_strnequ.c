#include "libft.h"

int	ft_strnequ(char const *s1, char const *s2, size_t n)
{
	size_t i;

	i = 0;
	if (n == 0)
		return (1);
	if (s1 && s2)
	{
		while (s1[i] == s2[i])
		{
			if (((s1[i + 1] == s2[i + 1]) && (s1[i + 1] == '\0')) ||
					((i + 1) == n))
				return (1);
			i++;
		}
	}
	return (0);
}
