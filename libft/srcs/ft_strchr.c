#include "libft.h"

char	*ft_strchr(const char *arr, int ch)
{
	size_t	i;

	i = 0;
	while (arr[i] != ch)
	{
		if (((char *)arr)[i] == '\0')
		{
			return (NULL);
		}
		i++;
	}
	return (&((char *)arr)[i]);
}
